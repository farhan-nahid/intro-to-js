// when to add value

let price = 50;
price = price + 10;
price += 10;

console.log(price);

// when we need to remove value

let price2 = 60;
price2 = price - 10;
price2 -= 10;

console.log(price2);

// when we need to increase the value by 1

let age = 15;
age = age + 1;
age += 1;
age++;

console.log(age);

// when we need to decrease the value by 1

let love = 100;
love = love - 1;
love -= 1;
love--;

console.log(love);
